    private void btnPesquisarCliente23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarCliente23ActionPerformed
        // cadastrar Pedidos cliente com cadastro
        
        SimpleDateFormat formatoBrasileiro = new SimpleDateFormat("dd/MM/yyyy");
        Date d = new Date();        
        String data = formatoBrasileiro.format(d);
        
        FuncionarioDAO fdao = new FuncionarioDAO();
        BebidaDAO bdao = new BebidaDAO();
        PratoDAO ppdao = new PratoDAO();
        Pedido p = new Pedido();
        PedidoDAO pdao = new PedidoDAO();
        Mesa m = new Mesa();
        MesaDAO mdao = new MesaDAO();
        ClienteDAO cdao = new ClienteDAO();
        
        
        cdao.addPorcentagemCliente((String) cbProdutospedido.getSelectedItem() , Integer.parseInt((String)cbQuantidade.getSelectedItem())  );        
        
        
        if(mdao.getStatusMesa( Integer.parseInt( (String)  cbMesas.getSelectedItem() ) )){
        
            p.setCodBebida( bdao.getIdBebida( (String) cbProdutospedido.getSelectedItem()  ) );//verificar codigo
            p.setCodPrato(  ppdao.getIdPrato( (String) cbProdutospedido.getSelectedItem()  ) );
            p.setCpf_Func(   fdao.getCPF( (String) cbFunc.getSelectedItem() )  ); // pega o cpf dando o nome do funcionario
            p.setId_mesa(Integer.parseInt( (String) cbMesas.getSelectedItem() ));
            p.setQuantidade(Integer.parseInt( (String) cbQuantidade.getSelectedItem() ));
            p.setCpf_Cliente(jCPFClientecad.getText());        
            p.setData(data);
            p.setCodigoPedido(  Integer.parseInt( jTexCodPedido.getText()  ));
            
            if(p.getCodBebida() != -1 ){
                pdao.cadastrarPedidoPrimeiroClienteComCadastroBebida(p);
            }else{
                pdao.cadastrarPedidoPrimeiroClienteComCadastroPrato(p);
            }

            m.setCodigo(p.getId_mesa());
            m.setNumPedidoAtual(p.getCodigoPedido());
            m.setStatus("ocupado");
            m.setDescricao("null");

            mdao.atualizarStatusMesa(m);
        }else{
            
            //JOptionPane.showMessageDialog(null, " esta ocupada ");
            
            
            
            p.setCodBebida( bdao.getIdBebida( (String) cbProdutospedido.getSelectedItem()  ) );//verificar codigo
            p.setCodPrato(  ppdao.getIdPrato( (String) cbProdutospedido.getSelectedItem()  ) );
            p.setCpf_Func(   fdao.getCPF( (String) cbFunc.getSelectedItem() )  ); // pega o cpf dando o nome do funcionario
            p.setId_mesa(Integer.parseInt( (String) cbMesas.getSelectedItem() ));
            p.setQuantidade(Integer.parseInt( (String) cbQuantidade.getSelectedItem() ));
            p.setCpf_Cliente(jCPFClientecad.getText());        
            p.setData(data);
            p.setCodigoPedido(mdao.getCodPedidoMesaOcupada( Integer.parseInt( jTexCodPedido.getText() )));
            
            
            if(p.getCodBebida() != -1 ){
                pdao.cadastrarPedidoPrimeiroClienteComCadastroBebida(p);
            }else{
                pdao.cadastrarPedidoPrimeiroClienteComCadastroPrato(p);
            }
        }
        
    }//GEN-LAST:event_btnPesquisarCliente23ActionPerformed

    private void btnPesquisarCliente19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarCliente19ActionPerformed
        // Listar pedidos sem cadastro
        
        
        Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;
        
                
        try {
            stmt = (PreparedStatement) con.prepareStatement("SELECT pedido.id_mesa as id , pedido.cpf_cliente as cpfc , pedido.cpf_func as cpff ,pedido.codigo as C , mesas.descricao as d FROM pedido , mesas WHERE mesas.codigo=pedido.id_mesa and mesas.status=? Order by pedido.codigo;");
            stmt.setString(1, "ocupado");
            
            rs = stmt.executeQuery();
            
            DefaultTableModel modelo = (DefaultTableModel) jTable4.getModel();
            modelo.setNumRows(0);
            
            while(rs.next()){
                    
                   Pedido m = new Pedido();
                   
                   m.setId_mesa(rs.getInt("id"));
                   m.setCpf_Cliente(rs.getString("cpfc"));
                   m.setCpf_Func(rs.getString("cpff"));
                   m.setCodigoPedido(rs.getInt("C"));                   
                   m.setDescricao(rs.getString("d"));
                   
                   
                  modelo.addRow(new Object[]{
                      
                      m.getId_mesa(),
                      m.getCpf_Cliente(),
                      m.getCodigoPedido(),
                     // m.getDescricao(),
                      m.getCpf_Func()                      
                    });  
                  
            }
                                   
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao busca: "+ex);
        } finally{
            ConnectionFactory.closeConnection(con, stmt, rs);
        }
        
        
    }//GEN-LAST:event_btnPesquisarCliente19ActionPerformed

