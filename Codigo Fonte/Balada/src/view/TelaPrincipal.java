    private void btnPesquisarFunc2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarFunc2ActionPerformed
        // lista todos os funcioanrios
        
        Funcionario f = new Funcionario();        
        Connection con = ConnectionFactory.getConnection();
        java.sql.PreparedStatement stmt = null;
        ResultSet rs = null;
        
        try {
            
            
            DefaultTableModel modelo = (DefaultTableModel) jTableFuncionario.getModel(); 
            modelo.setNumRows(0);
            
            stmt = (java.sql.PreparedStatement) con.prepareStatement("select * from funcionario;");
            
            rs = stmt.executeQuery();
            
            while(rs.next()){
                
                f.setNome(rs.getString("nome"));
                f.setTelefone(rs.getString("telefone"));
                f.setEndereco(rs.getString("endereco"));
                f.setSalario(rs.getFloat("salario"));
                f.setDataEntrada(rs.getString("dataEntrada"));
                f.setCargo(rs.getString("cargo"));
                
                        
                  modelo.addRow(new Object[]{
                        f.getNome(),
                        f.getTelefone(),
                        f.getEndereco(),
                        f.getSalario(),
                        f.getCargo(),
                        f.getDataEntrada()
                                          
                  });
                
            }
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Pesquisar funcionario: "+ex);
        } finally{
            ConnectionFactory.closeConnection(con, stmt);
        }
        
        
    }//GEN-LAST:event_btnPesquisarFunc2ActionPerformed

